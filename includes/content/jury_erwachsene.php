    <h3>Der Walter Kempowski Literaturpreis,</h3>
    <p>ein Förderpreis der Hamburger Autorenvereinigung, wird seit 2005 alle zwei Jahre an deutschsprachige
      Autorinnen und Autoren vergeben. Der Preis ist dazu gedacht bisher unbekannte Autorinnen und Autoren zu fördern.
      Stifter ist die Hannelore und Helmut Greve Stiftung für Kultur und Wissenschaft.</p>
    <div class="two-col-grid">
      <p>2011</p>
      <p> werde ich zum vierten Mal in der Hauptjury mitwirken.</p>
      <p>2013 </p>
      <p> gehörte ich, neben Gisela Knabbe, Anna Calina, Wolf-Ulrich Cropp und Reimer Eilers, wieder zur Hauptjury. Das diesjährige Thema: &quot;Besser geht's nicht&quot;.</p>
      <p>2015</p>
      <p> wirkte ich wieder mit Susanne Bienwald, Wolf-Ulrich Cropp, Reimer Eilers und Peter Schmid in der Hauptjury. Das Thema war: &quot;Nur eine Minute&quot;.</p>
      <p>2017 </p>
      <p> war das Thema: "Alles umsonst". Die fünf Juroren blieben dieselben wie 2015.</p>
      <p>2019 </p>
      <p> gehörten zur Hauptjury wieder dieselben wie im Vorjahr. Dieses Mal war das Thema: "Schöne Aussichten". Die Vorjury hatte wie in allen vorherigen Jahren aus fast Tausend Einsendungen 35 Geschichten und die Hauptjury daraus drei Gewinner ermittelt.</p>
      <p>2021 </p>

      <p> gehörte <b>Jutta Heinrich</b> leider nach vielen Jahren nicht mehr zur Vorjury, weil sie an 23. Juli 2021 völlig überraschend verstorben war.
        Nicht nur die Mitglieder der HAV, sondern die gesamte, nicht nur hamburgerische Literaturszene und der literarische Nachwuchs, den Jutta Heinrich
        sehr gefördert hatte, vermissen sie sehr. Ebenso die Mitglieder der HAV Vorjury ( Annegret-Witt-Bartel, Chalotte Ueckert ) und der Vorstand des
        Literarurzentrums im Literarturhaus Hamburg. Jutta Heinrich war die 1. Vorsitzende.</p>
    </div>
    <p> Die Preisverleihung für den 20. Mai 2022 wurde schon mehrmals verschoben. Doch aufgeschoben ist ja nicht aufgehoben. Die drei Finalisten wurden schon auf der<br />
      HAV Homepage <a href="https://www.hh-av.de" target="_blank">HOME</a> bekanntgegeben.</p>

    <p>Die Reihenfolge der Preisträger und Preisträgerinnen wird wie immer durch eine Lesung der Siegertexte vor Publikum und anschließender Abstimmung bestimmt.<br />
      Stimmberechtigt sind alle Mitglieder der Hamburger Autorenvereinigung.
    </p>
