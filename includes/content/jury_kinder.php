    <h2>Stiftung Mittagskinder 12. Juni 2012 </h2>
    <p>Knisternde Spannung herrschte im Saal: Mit Glöckchen wurde – zum fünften Mal in Folge! - der Vorlesewettbewerb unserer Lesekinder eingeläutet. Zwei Jungen und 16 Mädchen zwischen sechs und elf Jahren lasen vor. Ihre Bücher hatten sie selbst ausgewählt. Spannendes und Lustiges war zu hören – vom &quot;Fliegenden Klassenzimmer&quot; über den &quot;Räuber Hotzenplotz&quot; bis zum &quot;Grüffelo&quot;. Kein leichter Job für die Jury, prominent besetzt u.a. mit Thorsten Schröder (Tagesschau) und der Kinderbuch-Autorin Ellen Sell. Nach anderthalb Stunden: Siegerehrung! Preise gab's für die Plätze 1 bis 3 jeder Altersgruppe. Alle anderen freuten sich über eine Teilnahmeurkunde und ein kleines Geschenk.<br />
