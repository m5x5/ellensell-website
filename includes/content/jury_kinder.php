    <h1>Vorlesewettbewerbs 2020/2021</h1>
    <p>Auch diesen Dezember traten jeweils zwei Klassensieger der 6.Klassen beim Schulentscheid des Vorlesewettbewerbs an.</p>
    <p>Die Jury, bestehend aus Mila, Marlene und Paul aus Jahrgang 10 hörte spannende, lustige und klassische Textpassagen. </br>
      Nach acht überzeugenden Vorstellungen und einer ausführlichen Beratung der Jury war eine Entscheidung getroffen. </br>
      Felix Hantusch aus der 6b konnte mit seiner einnehmenden und flüssigen Darstellung am meisten überzeugen.</p>
    <p><b>Wir gratulieren !</b></p>
    <p><img src="includes/img/vorlesewettbewerb20_21.jpg" /></p>
    <p>Am Donnerstag, den 31.03. hat der Schulentscheid des Lesewettbewerbs der 6. Klassen am
      Gymnasium Meiendorf (kleine Aula) stattgefunden. Ein Schüler vom Gymnasium Meiendorf
      namens Paul Friesike, der den Klassen- und Stufenentscheid gewonnen hat, hat das GyMei
      bei der Veranstaltung vertreten. Er war einer von den elf Teilnehmern. Geleitet wurde die
      Veranstaltung von Frau Müller und Frau Romppanen. Die Jury waren ein Autor, eine Buchhändlerin und ein Autorin.
      Die Schülerinnen und Schüler mussten jeweils einen schon bekannten Text, den sie sich im
      Vorwege selbst aussuchen durften, und einen vorgegebenen vorlesen. Die Texte durften
      höchstens drei Minuten lang sein, man wurde durch einen sanften Glockenschlag darauf aufmerksam gemacht, dass man aufhören soll. Die meisten Schüler haben das Vorlesen gut gemeistert, jedoch haben sich bei manchen ein paar Fehler eingeschlichen. Insgesamt hat der
      Wettbewerb ca. 2 Stunden gedauert. Wegen der Länge des Wettbewerbes gab es zwei Pausen
      von ca. 15 Minuten, in denen man essen und trinken oder rausgehen konnte, um frische Luft
      zu schnappen. Am Ende wurde der Sieger gekürt. Gewonnen hat ein Junge namens Felix, den
      zweiten Platz belegte Paul vom GyMei.
    </p>
