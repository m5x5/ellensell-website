<p align="left">Als anschließend in der Vorlesegruppe mit zehn Kindern, Herrn Fellenberg und den Sozialpädagoginnen,
 die kleine Elena mein Filippo-Buch zu Ende las – weil es ihr so gut gefallen hatte – habe ich mich sehr gefreut.</p>
<p align="left">In den wöchentlichen Vorlesegruppen erhalten die Kinder, immer wenn sie teilnehmen, einen Stempel 
 in ihre persönliche Lesekarte, wobei es unwichtig ist, ob sie in den dafür vorgesehenen 20 Minuten
  für sich alleine oder mit Unterstützung einer der Sozialpädagogen gelesen haben. Nach 10 Stempeln
 (die Motive und Farben suchten sie sich mit Begeisterung selbst aus), bekommen sie ein Buch ihrer Wahl geschenkt.</p>
<p align="left">Und dann hieß es für mich Abschied zu nehmen. In der Küche dekorierten gerade Elena (aus Russland)
 und Marie (aus Togo) mit Hingabe Käse- und Aufschnittsplatten für das gemeinsame Abendessen. </p>
<p align="left">Als ich an der Ampel stand und mich noch einmal zum Kindertreff umsah, stand der  kleine Isa,
 dessen Mutter von der Dominikanischen Republik nach Neuwiedenthal kam, lächelnd am Zaun und winkte mir zu.</p>
