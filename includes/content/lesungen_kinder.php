<h3 align="left">Mein Besuch mit Lesung im Kindertreff Neuwiedenthal <br />
  <br />
  der Stiftung Mittagskinder<br />
</h3>
<p align="left">Vor drei Jahren brachte ein Artikel im Hamburger Abendblatt und mein neu erschienenes Kinderbuch &quot;Pauline Knabberschreck&quot;, die Stiftung Mittagskinder mit ihren Kindertreffs in Neuwiedenthal sowie Kirchdorf-Süd und mich zusammen.<br />
  Diese Stiftung betreut Kinder vom Vorschulalter bis zur Beendigung der Grundschule. Neben gemeinsamen Mahlzeiten, sportlichen und kulturellen Aktivitäten gibt es auch verschiedene Bildungsangebote.<br />
  Mich als Autorin begeisterten vor allem die Vorlesegruppen, die in den Kindertreffs einmal wöchentlich stattfinden. Höhepunkt des Lesejahres ist immer der jetzt gerade bevorstehende Vorlesewettbewerb.<br />
  Als mich die Vorsitzende des Stiftungsvorstandes, Frau Susann Grünwald-Aschenbrenner 2010 fragte, ob ich beim Vorlesewettbewerb in der Hauptjury dabei sein möchte, sagte ich natürlich gerne zu.</p>
<p align="left">Am Patentag der Budnianer Hilfe e.V. im Herbst 2011 verkauften die Diplom-Sozialpädagogen Nils Fellenberg und Caroline Krauss in der Budni-Filiale der Europapassage gesponserte, mit Budniprodukten gefüllte &quot;Wundertüten&quot; - und ich Kinderbücher zugunsten der &quot;Mittagskinder&quot;. Bei dieser Gelegenheit lud mich Nils Fellenberg in den Kindertreff Neuwiedenthal ein, dessen Leitung  er hat.<br />
  Am 9. Mai 2012 war es soweit. Das Navi führte mich stressfrei vom Osten in den Süden Hamburgs. Ich fuhr durch Alt-Neuwiedenthal, als mein Navi mir mitteilte, dass ich nach 200 Metern rechts abbiegen sollte. Gleich danach kam die Meldung:&quot;Sie haben Ihr Ziel erreicht. Es befindet sich auf der linken Straßenseite.&quot;<br />
  Ich fand zwar sofort einen Parkplatz, aber keine Spur vom Kindertreff. Ratlos stand ich vor einem Mehrfamilienhaus an einer Straßenkreuzung. Ich hörte Kinder lachen und sprechen. Sollten das etwa die Mittagskinder sein? Und richtig! Hinter einem Zaun entdeckte ich in einer abgesenkten, terrassenartigen Gartenanlage spielende Kinder; an einer Hauswand im Souterrain über einer Tür das Logo der Stiftung. <br />
  Kaum hatte ich die Anlage betreten, kamen auch schon Kinder auf mich zu, einige sogar mit Büchern in den Händen und fragten, ob ich Ellen Sell sei. <br />
  Dann erschien Nils Fellenberg. Er begrüßte mich und führte mich durch die Anlage, vorbei an einer Skulptur und einem Spielgerät. Überall spielende Kinder, einige kletterten herum, andere unterhielten sich angeregt, alle waren richtig fröhlich.<br />
  Die besonders Übermütigen wurden von ihm zur Vorsicht ermahnt und zu mir gewandt, fügte er hinzu: &quot;Ja, nur wer sich an die Regeln des Kindertreffs hält, darf bleiben. Kinder, die sich z.B. weigern, die Hausaufgaben zu machen, werden  von uns nach Hause geschickt.&quot; <br />
  Mir gefiel seine konsequente Art. Den Kindern offensichtlich auch, denn es herrschte eine angenehme, harmonische Atmosphäre nicht nur draußen, sondern auch in den Räumen des Kindertreffs. <br />
  Dort stellte mir Herr Fellenberg zwei Sozialpädagoginnen vor, die unter anderem für die Leseförderung zuständig sind, sowie zwei weitere sozialpädagogische Mitarbeiterinnen. Und um uns herum standen viele Kinder, mit denen wir uns unterhielten. <br />
  Mich beeindruckte ein Raum, in dem mir sofort das gut bestückte Bücherregal ins Auge fiel. Dort sollte wenig später die Lesung stattfinden.</p>
<p>&nbsp;</p>

<p align="left">Zuerst lasen abwechselnd Josefine und Melanie
  (beide 7 Jahre/ 2. Klasse) das ganze &quot;Güffelo&quot;-Buch vor
  - ohne einen einzigen Versprecher!!!</p>
<p align="left"><img src="includes/img/rapportbild_1.jpg" alt="" width="310" height="202" hspace="30" /></p>

<p align="left">Nach dieser beeindruckenden Leistung, lasen Abigail (10 Jahre/ 4. Kl.), Nalin und Jennifer (9 Jahre/ 3. Kl.) 
nacheinander den &quot;ersten Brief&quot; aus &quot;Pauline Knabberschreck&quot; genauso toll!!!</p>
<p align="left"><img src="includes/img/rapportbild_2.jpg" alt="" width="310" height="221" hspace="30" /></p>

<p align="left">Danach war ich an der Reihe. Ich las aus meinem neuesten Buch: &quot;Filippo, das Glühwürmchen&quot;.</p>
