    <h4><em>Hamburger Abendblatt (26.3.1994)</em></h4>
    <p>&quot;Hamburg – Immer wieder haben Schriftsteller die eigene Kindheit als literarisches Thema entdeckt. Bücher wie zum Beispiel Christa Wolfs &quot;Kindheitsmuster&quot; oder &quot;Die Verteidigung der Kindheit&quot; von Martin Waiser belegen die Bedeutung dieser Erinnerungen für die Gegenwartsliteratur.<br />
      <br />
      Auch <strong>Kindertage</strong>, die Anthologie, die die Hamburger Autorenvereinigung jetzt gemeinsam mit dem Verlag Langen Müller
      im Lichtwarksaal vorgestellt hat, vereinigt Texte von Schriftstellern, die schreibend der eigenen Kindheit auf die Spur zu kommen versuchten.
    </p>
    <p>Rosemarie Fiedler-Winter, die Vorsitzende der vor 17 Jahren gegründeten Schriftstellervereinigung, schrieb in ihrem Vorwort, die in dem Band vereinigten Mitglieder der &quot;Hamburger Autorenvereinigung&quot; wüßten um die besondere Wirkung von Kindheitserinnerungen auf ein großes Publikum. Sie führten &quot;Leser, Hörer und Schreiber dichter zusammen als manche andere Gelegenheit&quot;.</p>
    <p>Der Band enthält u.a. Texte von Hans Eppendorfer, Walter Kempowski, Gabriel Laub, Siegfried Lenz, Ellen Sell, Arno Surminski und Gerlind Fischer-Diehl.&quot;</p>
