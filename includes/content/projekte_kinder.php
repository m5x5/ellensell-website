	<h2>Loreley und das reitende Huhn,</h2>
<strong>	ein Gedicht für Kinder ab 6 Jahren.</strong>
	<p>Am Rande eines Dorfes, in der Lüneburger Heide,<br />
Steht der Eichenhof inmitten einer Weide.<br />
Dort leben die Schmitts mit ihren Kindern,<br />
Und vielen Tieren, sogar Angus-Rindern.<br />
</p>
	<p>An einem Frühlingstag liegt Kater Brummi dösend in der Sonne,<br />
Und Dini, die Hündin, schlappert aus der Regentonne.<br />
Auf der Koppel gleich nebenan,<br />
Beobachtet Oskar, der zottige Heidschnuckenmann,<br />
</p>
	<p>Mit Schuffchen, das ist seine Frau,<br />
Die gemeinsamen Kinder ganz genau:<br />
Flippi, das Böckchen, es ist etwas älter als ein Jahr,<br />
Und Hoppsi und Sause, das sechs Tage alte Zwillingspaar.<br />
</p>
	<p>Das hatte Frau Schmitt morgens im Gras entdeckt,<br />
Und sofort ihre Kinder Jule und Jan geweckt.<br />
Schuffchen hatte es in einer kalten Nacht,<br />
Ganz allein zur Welt gebracht.<br />
</p>
	<p>Wenn die Frühlingstage kühler sind,<br />
Schützt die Lämmer ihr Fell vorm Wind.<br />
Sie saugen gern am Euter ihrer Mutter,<br />
Springen umher und blöken nach Futter.<br />
</p>
	<p>Sammy ist neugierig wie alle Rinder,<br />
Und beobachtet die Heidschnuckenkinder.<br />
Er ist ein Kälbchen, von Elsa das Kind,<br />
Wäre gern dort, wo Flippi, Hoppsi und Sause sind.<br />
</p>
	<p>Nun rennt Sammy am Zaun im Trab,<br />
Immer wieder auf und ab.<br />
Zur Herde gehört auch die Leitkuh Loreley,<br />
Die futtert seit Monaten schon für zwei.<br />
</p>
<p><img width="" height="" src="includes/img/lorelei_kuh.jpg" alt="" onmouseover="" onmouseout="src='includes/img/lorelei_kuh.jpg'" hspace="" align="center" /></p>

	<p>Sie bekommt bald ein Kind, mit dem Sammy spielen kann,<br />
Dann braucht er nicht mehr die Schnucken von nebenan.<br />
Wie einfallsreich oft ältere Tiere sind,<br />
Bewies Loreley, das unternehmungslust'ge Rind.<br />
</p>
	<p>Es ereignete sich vor einem halben Jahr,<br />
Als Bulle Nelson bei ihr auf der Weide war. <br />
Der Mond schien in jener Nacht besonders hell<br />
Da juckte Loreley wohl ihr dickes Rinderfell.<br />
</p>
	<p>Sie trampelte den Zaun zu Brei,<br />
Schwuppdiwupp! Schon war sie frei!<br />
Zur Straße galoppierte das schlaue Tier,<br />
Der Bulle Nelson folgte ihr.<br />
</p>
	<p>Elsa und Sammy dachten vielleicht: Jetzt oder nie!<br />
Denn sie machten das gleiche wie die.<br />
So zogen vier Rinder im Gänsemarsch von dannen,<br />
Verschwanden in einem Wald voller Tannen.<br />
</p>
	<p>Danach lockte die Wiese<br />
Von Bauer Griese.<br />
Im Morgengrauen entdeckten sie sein Haus<br />
Und futterten im Garten, oh welch ein Graus!<br />
</p>
	<p>„Herrjemine!", rief Bauer Griese. „Das sind ja meine Kühe!"<br />
Doch als er sie trieb ins Gatter mit sehr viel Mühe,<br />
Zeigte Nelson, dass er keine Kuh, sondern ein Bulle ist,<br />
Und kam wie ein wild gewordner Stier auf ihn zugeflitzt,<br />
</p>
	<p>Bauer Griese schrie: „ Donner und Blitz!<br />
Den sah ich gestern auf der Weide von Schmitts.<br />
Als er sie anrief, fielen sie vor Schreck aus den Betten,<br />
„Die Rinder", drohte Vater Schmitt, „kommen an Ketten!"<br />
</p>
	<p>Neben Flippi, Hoppsi und Sause,<br />
Haben die Hühner ihr Zuhause.<br />
In der kleinen Hühnerschar,<br />
Ist die Henne Ottilie der Star.<br />
</p>
	<p>Ottilies Po ist weich wie ein Sofakissen,<br />
Das scheint auch Gorr, der Hahn zu wissen.<br />
Die Henne Schnupfi sieht er kaum an,<br />
Er ist am liebsten nur Ottilies Mann.<br />
</p>
	<p>Doch eines Tages fand Schnupfi im Zaun ein Loch,<br />
Durch das sie zu den Heidschnucken kroch.<br />
Und damit begann,<br />
Die Freundschaft vom Huhn und Schnuckenmann.<br />
</p>

<p><img width="" height="" src="includes/img/scharf_huhn.jpg" alt="" onmouseover="" onmouseout="src='includes/img/scharf_huhn.jpg'" hspace="" align="center" /></p>
	<p>Die Henne, die beim Hahn keine Beachtung fand,<br />
Eines Tages auf Oskars Rücken stand. <br />
Als Jan auf der Weide hatte zu tun,<br />
Sah er Schnupfi, das reitende Huhn.<br />
</p>

	<p>Ganz anders liegt der Fall,<br />
Im immer lauten Entenstall.<br />
Dort schnattern um die Wette <br />
Die Enten Tussi, Mini und Jette<br />
</p>
	<p>In den Schnatterchor fallen mit ein,<br />
Die Entchen Stine, Olga und Hein.<br />
Ihr Vater ist Donald, ein schwarz-weißer Enterich.<br />
Um seine Kinder kümmert er sich leider nicht.<br />
</p>
	<p>Mit viel mehr Geschnatter,<br />
Lebt im gleichen Gatter,<br />
Ein selten schönes Gänsepaar,<br />
Petunius und Petunia.<br />
</p>
	<p>Jule war vor ein paar Jahren,<br />
Allein in die Stadt gefahren.<br />
Ein Viehmarkt lockte sie magisch an.<br />
Und dann entdeckte sie Donald, den Entenmann!<br />
</p>
	<p>Er war winzig, sah wirklich hässlich aus,<br />
Aus Mitleid nahm sie ihn mit nach Haus.<br />
Petunius und Petunia umsorgten ihn wie Vater und Mutter,<br />
Gaben ihm Wärme und zeigten ihm das beste Futter.<br />
</p>
	<p>Petunia ist eine schneeweiße Gans,<br />
Petunius ist grau, vom Kopf bis zum Schwanz.<br />
Donald watschelt ihnen immer hinterher,<br />
Er mag Gänse, Enten nicht so sehr.<br />
</p>
	<p>Weil Jules und Jans Oma lebt in der Stadt,<br />
Und sie deshalb keine Tiere hat,<br />
Ist sie immer von Herzen froh,<br />
In Schmitts kleinem Heidezoo.<br />
</p>
	<p>Mit Dini geht sie heute zum Federvieh.<br />
Petunius empfängt sie; aber wie?!<br />
Vielleicht fühlt er sich von ihnen bedroht,<br />
Auf jeden Fall sieht er plötzlich rot!<br />
</p>
	<p>Denn zuerst beißt er Dini ins Bein,<br />
Danach in Oma Hannas Nase hinein.<br />
Jaulend rennt Dini ins Haus,<br />
Und die Oma schimpft:"Petunius, unsre Freundschaft ist aus!"<br />
</p>
	<p>Es ist noch wichtig, zu erfahren:<br />
Dini ist eine Schäferhündin von zwei Jahren.<br />
Ein Ohr trägt sie schlapp, das andere nach oben,<br />
Am liebsten mag sie mit den Kindern toben.<br />
</p>
	<p>Doch wenn es niemand sieht,<br />
Was hin und wieder geschieht,<br />
Stürmt sie wie ein Wirbelwind,<br />
Durch Federvieh und Rind.<br />
</p>
	<p>Dann fängt das erste Tier an zu schrein,<br />
Und alle andern fallen ein:<br />
„Muh, muh, gack, gack, kikeriki,<br />
Mär - mä-ä-är zetern sie.<br />
</p>
	<p>Jule erlöst dann das arme Getier<br />
Und schimpft ganz fürchterlich mit ihr:<br />
„Dini, du darfst kein Tier erschrecken<br />
Und Petunius niemals necken!"<br />
</p>
	<p>Vielleicht denkt Dini: "Warum sprichst du so zu mir?<br />
Es ist doch nur ein Spiel, das weiß hier jedes Tier.<br />
Deshalb wird sie wohl nach ein paar Tagen,<br />
Es wieder aufs Neue wagen.<br />
</p>
	<p>Vorerst aber kehrt Ruhe ein.<br />
Nur das Entchen Hein<br />
Sucht piepsend seine Familie,<br />
Während das Huhn Ottilie geschickt<br />
Körner vom Boden pickt. <br />
</p>
<p><img width="" height="" src="includes/img/ente.jpg" alt="" onmouseover="" onmouseout="src='includes/img/ente.jpg'" hspace="" align="center" /></p>
	<p>Und auf der Weide, im warmen Sommerwind,<br />
Schläft neben Dini ein Heidschnuckenkind.<br />
</p>
<p>Alle Tiere und Menschen in diesem Gedicht lernte ich persönlich kennen, die Namen der Tiere habe ich nicht verändert, wohl aber die der Menschen.<br />
Wir hatten in der Nähe des Eichenhofes der Familie Schmitt ein Ferienhaus, und Frau Schmitt machte aus einem Spieker, in dem früher Heu gelagert wurde, einen Trödlerladen.<br />
Dadurch und durch die Tierliebe der Familie wurden wir Freunde.</p>
