    <h3 align="left">Kater Pfennig </h3>
    <p align="left"><img src="includes/img/mini_kater_pfennig.jpg" width="62" height="100" hspace="20" align="left" />
      rabenschwarz hat es gut. Er kommt in eine Familie, wo ihn alle mögen...&nbsp;
      Auf liebevolle Art und Weise wird dem Leser durch dieses Buch mitgeteilt,
      welche Verantwortung es mit sich bringt, eine Katze ins Haus zu holen.
      Sehr einfühlsam wird erzählt von all den Eigenarten und Eigensinnigkeiten der Katzen.
      Viel besser als den mahnenden oder gar ablehnenden Worten vieler Eltern gelingt es dem Buch,
      den Kindern das Leben mit einer Katze aufzuzeigen. Es ist nicht nur schön,
      sondern es gibt auch viele problematische Situationen, denn die Katze ist kein Plüschtier!</p>
