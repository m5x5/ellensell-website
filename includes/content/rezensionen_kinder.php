    <h3 align="left">Yvonne Stachelbeere</h3>
    <p align="left"><img src="includes/img/mini_yvonne_stachel.jpg" width="64" height="100" hspace="20" align="left" />
      &quot;Eine unterhaltsame Geschichte, die mit den Hauptpersonen Hanna, Daniel
      und den drei Kindern der Familie Bucher für viel Abwechslungsreichtum sorgt.
      Dabei spielt die Darstellung der Gefühle der einzelnen Personen (neben der meist
      turbulenten Handlung) eine bedeutene Rolle. Ob es sich dabei um Hannas Heimweh
      und ihre Beziehungsentwicklung zu Daniel oder um die Sorgen und Freuden der
      Kinder der Arztfamilie handelt, bei der Hanna&nbsp;arbeitet – der Leser kann
      an allem Anteil nehmen. Inhaltlich und sprachlich kann ich dieses Buch als
      Lektüre für Zehnjährige empfehlen: für Mädchen (Yvonne) gib es gleihermaßen wie
      für Jungen (Thomas, Peter) die Möglichkeit, sich mit im Buch vorkommenden
      Personen zu identifiziieren. Beleitet werden die einzelnen Kapitel mit lustigen
      Schwarz-Weiß-Zeichnungen.&quot;</p>
