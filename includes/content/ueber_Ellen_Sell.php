    <h2>Vita</h2>
    <p align="left"><img src="includes/img/Ellen Sell75678.JPG" alt="Image" /></p>
    <p>Ich wurde in Hamburg geboren und wuchs mit meinem Bruder ohne unseren Vater auf, der 1943 in Russland gefallen war. Die Kriegs- und Nachkriegszeit prägte meine Kindheit: Blockflöte spielen, Schlittschuh laufen und Lehrerin werden blieben für mich unerfüllte Träume.

      Ich wurde Industriekauffrau, heiratete den Dipl. Ingenieur (Statiker und Architekt) Artur Sell und bekam zwei Töchter. Meine Älteste arbeitet an der Sorbonné Université als Organisationsberaterin und meine Jüngste als Drehbuchautorin, Regisseurin und Fotografin in Berlin.
      Erst nachdem meine Töchter das Elterhaus verlassen hatten, führte die Liebe zu Kindern – nicht nur zu den eigenen –, zu Tieren und zum Schreiben 1993 zum ersten Kinderbuch beim Ensslin-Verlag.

      1994 gelang mir mit Kurzprosa der Einstieg in die Literatur für Erwachsene.

      Ich bin aktives Mitglied der Hamburger Autorenvereinigung, des Literaturzentrums Hamburg e.V. im Literaturhaus Hamburg, in der Gedok und im Li-Autorenverzeichnis.</p>
